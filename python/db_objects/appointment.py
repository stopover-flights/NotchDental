class Appointment:
    def __init__(self, id, time, listed_price, full_price, filled, practice_id, patient_id, service_id, is_active):
        Appointment.id = id
        Appointment.time = time
        Appointment.listed_price = listed_price
        Appointment.full_price = full_price
        Appointment.filled = filled
        Appointment.practice_id = practice_id
        Appointment.patient_id = patient_id
        Appointment.service_id = service_id
        Appointment.is_active = is_active

